package com.entities;

public class Schedule extends InformationDate{
    private int id;
    public String name;

    public Schedule(){

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

}
